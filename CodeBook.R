Data Cleaning Project

The data cleaning project script run_analysis.R attempts to do the following:
- download the zip file containing smartphone data
- extract the data (which contains test and training data sets)
- merge the test sets into one dataset
- merge the training sets into one dataset
- combine both datasets into one master
- determine an average of all the mean and standard deviation measures
	across user and activity
-export the resulting tidy data to CourseProject.txt

